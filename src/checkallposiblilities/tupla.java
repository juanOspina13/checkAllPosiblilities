public class tupla {
    String organismo1;
    String organismo2;

    public tupla(String organismo1, String organismo2) {
        this.organismo1 = organismo1;
        this.organismo2 = organismo2;
    }

    public String getOrganismo1() {
        return organismo1;
    }

    public String getOrganismo2() {
        return organismo2;
    }

    public void setOrganismo1(String organismo1) {
        this.organismo1 = organismo1;
    }

    public void setOrganismo2(String organismo2) {
        this.organismo2 = organismo2;
    }
    
    
    
}
